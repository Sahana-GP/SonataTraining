package com.sonata;

public class LogicalOps {

	public static void main(String[] args) {
		
		    System.out.println((7<10)&&(10>5));
		    System.out.println((7>10)&&(10>5));
		    System.out.println();
		    
		    System.out.println((12>2)||(2>1));
		    System.out.println((12<2)||(2>1));
		    System.out.println((12<2)||(2<1));
		    System.out.println();
		    
		    System.out.println(!(7==7));   
		    System.out.println(!(2>3));
		  }
	}


