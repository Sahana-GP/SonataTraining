public class IfElseCondition {

	public static void main(String[] args) {
		int i = 5;
		 
        if (i < 10) {
        	System.out.println("5 is less than 10\n");
        }
        else {
        	System.out.println("5 is not less than 10");
        }
        
        System.out.println("Outside if-block");
	}

	

}
